#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	char	*b2;
	char	c2;
	size_t	i;

	b2 = (char*)b;
	c2 = (unsigned char)c;
	i = 0;
	while (i < len)
	{
		b2[i] = c2;
		i++;
	}
	return (b2);
}
